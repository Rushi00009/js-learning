var n1 = 10;
var n2 = 20;
var result = n1 + n2;
console.log(`Addition is : ${result}`);

console.log("========== Subtraction - operator ==========");
var n1 = 20;
var n2 = 10;
var result = n1 - n2;
console.log(`Subtraction is : ${result}`);

console.log("========== Multiplication * operator ==========");
var n1 = 5;
var n2 = 5;
var result = n1 * n2;
console.log(`Multiplication is : ${result}`);

console.log("========== Exponential  ** operator ==========");
var n1 = 2;
var n2 = 2;
var result = n1 ** n2;
console.log(`Exponential is : ${result}`);

console.log("========== Division / operator ==========");
var n1 = 2;
var n2 = 2;
var result = n1 / n2;
console.log(`Division is : ${result}`);

console.log("========== Modulus % operator ==========");
var n1 = 2;
var n2 = 2;
var result = n1 % n2;
console.log(`Reminder is : ${result}`);

console.log("========= Decrement --  operator ==============");
var n1 = 10;
var result = --n1 // n1 = n1 - 1  // Prefix 
console.log(`Result is: ${result}`);

var n2 = 20;
var resultValue = n2--; // n2 = n2 - 1 Postfix 
console.log(`Result is: ${resultValue}`);