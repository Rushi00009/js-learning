console.log("======= Swaping Two values using third variable =======");
var sweety = "Sweety";
var cutie = "Cutie";
console.log("==== Before Swap===");
console.log("Sweety", ":", sweety, "Cutie", ":", cutie);
console.log("=== After Swap===");
var temp = sweety;
sweety = cutie;
cutie = temp;
console.log("Sweety", ":", sweety, "Cutie", ":", cutie);
console.log("==========================================");

console.log("======= Swaping Three values using third variable =======");
var n1 = 100;
